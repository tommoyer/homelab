def _normalized_service_filename(service_name: str) -> str:
    raw = service_name.strip().lower()
    if not raw:
        return "service"
    normalized = re.sub(r"[^a-z0-9._-]+", "-", raw)
    normalized = normalized.strip("-_.")
    safe = sanitize_filename_component(normalized)
    return safe or "service"


def _protocol_variant_service_name(service_name: str, protocol: str) -> str:
    base_name = service_name.strip() or "service"
    suffix = f" - {protocol.upper()}"
    if base_name.endswith(suffix):
        return base_name
    return f"{base_name}{suffix}"


def _protocol_variant_service_key(service_key: str, service_name: str, protocol: str) -> str:
    base_key = service_key.strip() or service_name.strip() or "service"
    suffix = f"---{protocol.lower()}"
    if base_key.endswith(suffix):
        return base_key
    return f"{base_key}{suffix}"


def _expand_protocol_variants(parsed: ParsedInput) -> list[ParsedInput]:
    if parsed.protocol in {"tcp", "udp"}:
        return [parsed]
    if parsed.ingress == "caddy":
        return [replace(parsed, protocol="tcp")]

    variants: list[ParsedInput] = []
    for protocol in ("tcp", "udp"):
        variants.append(
            replace(
                parsed,
                protocol=protocol,
                service_key=_protocol_variant_service_key(parsed.service_key, parsed.service_name, protocol),
                service_name=_protocol_variant_service_name(parsed.service_name, protocol),
            )
        )
    return variants


def _format_debug_value(value: Any) -> str:
    if value is None:
        return "(none)"
    if isinstance(value, bool):
        return "true" if value else "false"
    if isinstance(value, list):
        return "[" + ", ".join(_format_debug_value(item) for item in value) + "]"
    if isinstance(value, tuple):
        return "(" + ", ".join(_format_debug_value(item) for item in value) + ")"
    if isinstance(value, dict):
        return "{" + ", ".join(f"{key}={_format_debug_value(val)}" for key, val in value.items()) + "}"
    return str(value)


