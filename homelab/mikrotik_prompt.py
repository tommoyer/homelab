def _generate_random_mac(prefix: str = DHCP_MAC_PREFIX) -> str:
    prefix_upper = prefix.strip().upper()
    random_hex = secrets.token_hex(3).upper()
    suffix = ":".join(random_hex[i:i + 2] for i in range(0, 6, 2))
    return f"{prefix_upper}:{suffix}"


