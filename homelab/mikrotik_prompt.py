def _classify_generated_commands(
    commands: list[str],
) -> dict[str, list[str]]:
    """Classify generated commands into buckets by type and filter chain.

    Returns a dict with keys: address_list, input_filter, forward_filter, nat, dhcp, other.
    """
    buckets: dict[str, list[str]] = {
        "address_list": [],
        "input_filter": [],
        "forward_filter": [],
        "nat": [],
        "dhcp": [],
        "other": [],
    }
    for command in commands:
        if command.startswith("/ip firewall address-list add"):
            buckets["address_list"].append(command)
        elif command.startswith("/ip firewall filter add"):
            if "chain=input" in command:
                buckets["input_filter"].append(command)
            else:
                buckets["forward_filter"].append(command)
        elif command.startswith("/ip firewall nat add"):
            buckets["nat"].append(command)
        elif command.startswith("/ip dhcp-server lease add"):
            buckets["dhcp"].append(command)
        else:
            buckets["other"].append(command)
    return buckets


def _dedupe_command_list(commands: list[str]) -> list[str]:
    seen: set[str] = set()
    result: list[str] = []
    for command in commands:
        key = _command_dedupe_key(command)
        if key in seen:
            continue
        seen.add(key)
        result.append(command)
    return result


def _build_ordered_filter_commands(
    manual_rules: list[ManualFilterRuleEntry],
    anchor_map: dict[str, list[str]],
    *,
    place_before_filter_comment: str | None = None,
    place_before_input_comment: str | None = None,
) -> list[str]:
    """Emit filter commands in spreadsheet row order, expanding anchors inline."""
    result: list[str] = []
    for entry in manual_rules:
        if entry.kind == "rule":
            result.append(
                _render_manual_filter_rule_command(
                    entry,
                    place_before_filter_comment=place_before_filter_comment,
                    place_before_input_comment=place_before_input_comment,
                )
            )
        elif entry.kind == "anchor" and entry.anchor_id:
            result.extend(_dedupe_command_list(anchor_map.get(entry.anchor_id, [])))
    return result


def _build_ordered_nat_commands(
    manual_rules: list[ManualNatRuleEntry],
    anchor_map: dict[str, list[str]],
) -> list[str]:
    """Emit NAT commands in spreadsheet row order, expanding anchors inline."""
    result: list[str] = []
    for entry in manual_rules:
        if entry.kind == "rule":
            result.append(_render_manual_nat_rule_command(entry))
        elif entry.kind == "anchor" and entry.comment:
            result.extend(_dedupe_command_list(anchor_map.get(entry.comment, [])))
    return result


def _command_group_name(command: str) -> str:
    if command.startswith("/ip firewall address-list add"):
        return "address-list"
    if command.startswith("/ip firewall filter add"):
        return "filter"
    if command.startswith("/ip firewall nat add"):
        return "nat"
    if command.startswith("/ip dhcp-server lease add"):
        return "dhcp"
    return "other"


def _command_dedupe_key(command: str) -> str:
    return re.sub(r'\scomment=(?:"(?:[^"\\]|\\.)*"|\S+)', '', command).strip()


def _dedupe_grouped_commands(commands: list[str]) -> list[str]:
    grouped: dict[str, list[str]] = {
        "address-list": [],
        "filter": [],
        "nat": [],
        "dhcp": [],
        "other": [],
    }
    seen_by_group: dict[str, set[str]] = {key: set() for key in grouped}

    for command in commands:
        group = _command_group_name(command)
        key = _command_dedupe_key(command)
        if key in seen_by_group[group]:
            continue
        seen_by_group[group].add(key)
        grouped[group].append(command)

    ordered_commands: list[str] = []
    for group in ("address-list", "filter", "nat", "dhcp", "other"):
        ordered_commands.extend(grouped[group])
    return ordered_commands


