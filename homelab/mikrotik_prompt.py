def _resolve_caddy_ip(
    *,
    caddy_ip_raw: str,
    sheet_url: str,
    nodes_gid: int,
    timeout_seconds: float,
    ip_column: str,
) -> str | None:
    """Resolve the Caddy IP from a raw value that may be a hostname or IP.

    If the value is already a valid IP it is returned directly.  Otherwise
    it is treated as a hostname and looked up in the Nodes sheet.
    """
    raw = (caddy_ip_raw or "").strip()
    if not raw:
        return None
    # Try as a literal IP first.
    direct = normalize_ip(raw)
    if direct:
        return direct
    # Fall back to Nodes sheet hostname lookup.
    return _resolve_hostname_ip_from_nodes(
        sheet_url=sheet_url,
        nodes_gid=nodes_gid,
        timeout_seconds=timeout_seconds,
        hostname=raw,
        ip_column=ip_column,
    )


def main(argv: list[str] | None = None) -> int:
    args = _build_parser(argv)
    configure_logging(debug=bool(args.debug))
    configure_sheet_csv_retention(keep=bool(args.keep))

    if not args.no_prompt and not args.caddy_dstnat_only and not _has_interactive_tty():
        print(
            "Error: interactive terminal required for curses UI (stdin/stdout must be a TTY).",
            file=sys.stderr,
        )
        return 2

    config = load_toml_or_exit(Path(args.config).expanduser().resolve())
    vlan_map = _parse_vlan_map(config)
    wan_ips, invalid_wan_ips = _parse_wan_ip_list(config)
    hairpin_source_entries, hairpin_source_issues = _load_hairpin_sources_from_zones_sheet(
        sheet_url=str(args.sheet_url),
        zones_gid=int(args.zones_gid),
        timeout_seconds=float(args.sheet_timeout),
    )
    hairpin_enabled = bool(hairpin_source_entries)

    lease_cfg = get_table(config, "mikrotik") or get_table(config, "dhcp_leases")

    lease_type_column = normalize_column_name(str(get_config_value(lease_cfg, "lease_type_column", "Static/DHCP")))
    ip_column = normalize_column_name(str(get_config_value(lease_cfg, "ip_column", "IP Address")))
    subnet_column = normalize_column_name(str(get_config_value(lease_cfg, "subnet_column", "Subnet")))
    mac_column = normalize_column_name(str(get_config_value(lease_cfg, "mac_column", "MAC Address")))
    vlan_id_column = normalize_column_name(str(get_config_value(lease_cfg, "vlan_id_column", "VLAN ID")))

    # ---- Resolve the Caddy IP (hostname or literal) via Nodes sheet ----
    caddy_ip_resolved = _resolve_caddy_ip(
        caddy_ip_raw=str(args.caddy_ip),
        sheet_url=str(args.sheet_url),
        nodes_gid=int(args.nodes_gid),
        timeout_seconds=float(args.sheet_timeout),
        ip_column=ip_column,
    )

    # ---- Standalone caddy-dstnat generation ----
    if args.caddy_dstnat_only:
        if not caddy_ip_resolved:
            print(
                f"Error: unable to resolve Caddy IP from '{args.caddy_ip}'. "
                "Provide a valid IP or a hostname present in the Nodes sheet.",
                file=sys.stderr,
            )
            return 1
        output_base = resolve_path_relative_to_config(
            Path(args.config).expanduser().resolve(),
            Path(args.output),
        )
        output_dir = output_base.parent if output_base.suffix else output_base
        caddy_dstnat_content = _render_caddy_dstnat(
            caddy_ip_resolved,
            enable_hairpin=False,
            wan_ips=wan_ips,
        )
        caddy_dstnat_path = output_dir / "caddy-dstnat.rsc"
        caddy_dstnat_path.parent.mkdir(parents=True, exist_ok=True)
        caddy_dstnat_path.write_text(caddy_dstnat_content, encoding="utf-8")
        print(f"Wrote {caddy_dstnat_path}")
        if hairpin_source_entries:
            hairpin_rules_path = output_dir / "hairpin-rules.rsc"
            hairpin_rules_path.write_text(
                _render_hairpin_rules(
                    source_entries=hairpin_source_entries,
                    candidates=[],
                ),
                encoding="utf-8",
            )
            print(f"Wrote {hairpin_rules_path}")
        if args.stdout:
            print(caddy_dstnat_content)
        return 0

    selection_options: dict[str, list[str]] = {}
    if not args.no_prompt:
        if int(args.helpers_gid) <= 0:
            print("Error: --helpers-gid is required and must be > 0.", file=sys.stderr)
            return 2

        try:
            helpers_values = _load_helpers_values(
                sheet_url=str(args.sheet_url),
                helpers_gid=int(args.helpers_gid),
                timeout_seconds=float(args.sheet_timeout),
            )
            selection_options = _build_selection_options(
                helpers_values=helpers_values,
                vlan_map=vlan_map,
            )
        except Exception as exc:
            print(f"Error: unable to load Helpers sheet values: {exc}", file=sys.stderr)
            return 1

        try:
            node_hostname_options = _list_node_hostnames_from_nodes_sheet(
                sheet_url=str(args.sheet_url),
                nodes_gid=int(args.nodes_gid),
                timeout_seconds=float(args.sheet_timeout),
            )
            if node_hostname_options:
                selection_options["hostname"] = node_hostname_options
        except Exception as exc:
            logger.warning("Unable to load Nodes sheet hostname options: %s", exc)

    service_values = [str(value).strip() for value in list(args.service or []) if str(value).strip()]
    selected_services: list[tuple[str, str]] = []
    seen_service_keys: set[str] = set()

    def _add_selected_service(service_key: str, source: str) -> None:
        normalized = service_key.strip()
        if not normalized:
            return
        dedupe_key = normalized.casefold()
        if dedupe_key in seen_service_keys:
            return
        seen_service_keys.add(dedupe_key)
        selected_services.append((normalized, source))

    if service_values:
        for service_value in service_values:
            _add_selected_service(service_value, "--service")

    if args.no_prompt and not selected_services:
        print("Error: --no-prompt requires at least one --service.", file=sys.stderr)
        return 2

    if not selected_services:
        service_options: list[str] = []
        try:
            service_options = _list_services_from_sheet(
                sheet_url=str(args.sheet_url),
                services_gid=int(args.services_gid),
                timeout_seconds=float(args.sheet_timeout),
            )
        except Exception as exc:
            logger.warning("Unable to load Services sheet for menu: %s", exc)

        if service_options:
            selections = _run_service_selection_menu(services=service_options)
            if selections == ["New..."]:
                manual_service_key = _run_single_input_prompt("")
                _add_selected_service(manual_service_key, "new")
            else:
                for service in selections:
                    _add_selected_service(service, "sheet")
        else:
            manual_service_key = _run_single_input_prompt("")
            _add_selected_service(manual_service_key, "new")

    if not selected_services:
        print("Error: at least one service key is required", file=sys.stderr)
        return 2

    # Read input_chain_hosts from config — hostnames whose services use
    # the INPUT chain (router/switch management UIs) instead of FORWARD.
    fw_cfg = get_table(config, "mikrotik") or get_table(config, "firewall")
    input_chain_hosts: list[str] = []
    raw_hosts = get_config_value(fw_cfg, "input_chain_hosts", [])
    if isinstance(raw_hosts, list):
        input_chain_hosts = [str(h).strip() for h in raw_hosts if str(h).strip()]

    place_before_filter_comment = str(
        get_config_value(fw_cfg, "place_before_filter_rule_comment", "")
    ).strip() or None
    place_before_input_comment = str(
        get_config_value(fw_cfg, "place_before_input_rule_comment", "")
    ).strip() or None

    output_base = resolve_path_relative_to_config(
        Path(args.config).expanduser().resolve(),
        Path(args.output),
    )
    output_dir = output_base.parent if output_base.suffix else output_base

    successes: list[Path] = []
    failures: list[tuple[str, str]] = []
    validation_issues: list[ValidationIssue] = []
    generated_filenames: set[str] = set()
    prepared_services: list[tuple[str, ServiceFormData]] = []

    for invalid_wan_ip in invalid_wan_ips:
        validation_issues.append(
            ValidationIssue(
                severity="error",
                message=f"invalid WAN IP configured in [mikrotik].wan_ips: {invalid_wan_ip}",
            )
        )
    validation_issues.extend(hairpin_source_issues)

    total_services = len(selected_services)
    for service_index, (service_key, selection_source) in enumerate(selected_services, start=1):
        prefill: dict[str, str | Any] = {}
        try:
            service_matches = _prefill_candidates_from_services_sheet(
                sheet_url=str(args.sheet_url),
                services_gid=int(args.services_gid),
                timeout_seconds=float(args.sheet_timeout),
                service_key=service_key,
            )

            if args.no_prompt:
                target = service_key.strip().lower()
                frontend_matches = [
                    match
                    for match in service_matches
                    if str(match.get("frontend_hostname") or "").strip().lower() == target
                ]
                if len(frontend_matches) > 1:
                    if not sys.stdin.isatty():
                        failures.append(
                            (
                                service_key,
                                (
                                    f"multiple services found for frontend hostname '{service_key}' "
                                    "and stdin is not interactive to select one"
                                ),
                            )
                        )
                        continue
                    prefill = _prompt_select_service_by_name(
                        service_key=service_key,
                        matches=frontend_matches,
                    )
                elif len(frontend_matches) == 1:
                    prefill = frontend_matches[0]
                elif service_matches:
                    prefill = service_matches[0]
            elif service_matches:
                prefill = service_matches[0]
        except Exception as exc:
            logger.warning("Unable to load services sheet prefill for %s: %s", service_key, exc)

        if args.no_prompt and not prefill:
            failures.append(
                (
                    service_key,
                    "service not found in Services sheet (required for --no-prompt)",
                )
            )
            continue

        form = ServiceFormData(service_key=service_key)

        for key, value in prefill.items():
            if hasattr(form, key) and value:
                setattr(form, key, value)

        try:
            nodes_prefill = _prefill_from_nodes_sheet(
                sheet_url=str(args.sheet_url),
                nodes_gid=int(args.nodes_gid),
                timeout_seconds=float(args.sheet_timeout),
                hostname=form.hostname,
                lease_type_column=lease_type_column,
                ip_column=ip_column,
                subnet_column=subnet_column,
                mac_column=mac_column,
                vlan_id_column=vlan_id_column,
            )
            for key, value in nodes_prefill.items():
                if hasattr(form, key) and value:
                    setattr(form, key, value)
        except Exception as exc:
            logger.warning("Unable to load nodes sheet prefill for %s: %s", service_key, exc)

        if not args.no_prompt:
            form = _run_form_ui(
                form,
                title=(
                    f"MikroTik service form ({service_index}/{total_services}) "
                    f"{service_key} [source: {selection_source}]"
                ),
                selection_options=selection_options,
            )
        prepared_services.append((service_key, form))

    if not prepared_services:
        for service_key, reason in failures:
            print(f"Failed service '{service_key}': {reason}", file=sys.stderr)
        print(
            f"Processed {len(selected_services)} service(s): "
            f"{len(successes)} succeeded, {len(failures)} failed"
        )
        return 1

    if not args.no_prompt:
        confirmed = _run_batch_confirmation_ui([form for _, form in prepared_services])
        if not confirmed:
            print("Aborted")
            return 130

    parsed_services: list[ParsedInput] = []
    for service_key, form in prepared_services:
        try:
            parsed = _normalize_form(
                form,
                vlan_map,
                caddy_ip=caddy_ip_resolved or "",
                input_chain_hosts=input_chain_hosts,
                place_before_filter_comment=place_before_filter_comment,
                place_before_input_comment=place_before_input_comment,
            )
        except ValueError as exc:
            failures.append((service_key, str(exc)))
            continue

        if parsed.enable_dhcp and not parsed.dhcp_server:
            if args.no_prompt:
                failures.append(
                    (
                        service_key,
                        (
                            "unable to determine DHCP server; missing VLAN ID for "
                            f"column '{vlan_id_column}' (expected server format dhcp-v<vlan-id>)"
                        ),
                    )
                )
                continue

            while True:
                print(
                    (
                        f"Enter DHCP VLAN ID for '{service_key}' "
                        "(server will be dhcp-v<vlan-id>): "
                    ),
                    end="",
                    flush=True,
                )
                entered_vlan_id = input().strip()
                parsed.dhcp_server = _dhcp_server_from_vlan_id(entered_vlan_id)
                if parsed.dhcp_server:
                    break
                print("Invalid VLAN ID. Enter a numeric VLAN ID (for example: 20).", file=sys.stderr)

        if parsed.enable_dhcp and parsed.dhcp_address and not parsed.dhcp_mac:
            parsed.dhcp_mac = _generate_random_mac()
            print(
                f"DHCP MAC not provided for '{service_key}'; generated random MAC {parsed.dhcp_mac}",
                file=sys.stderr,
            )

        parsed_services.append(parsed)

    public_pipeline = _build_public_nat_pipeline(
        parsed_services,
        wan_ips=wan_ips,
    )
    hairpin_candidates = _collect_public_candidates_for_hairpin(public_pipeline)
    validation_issues.extend(public_pipeline.issues)

    if not hairpin_enabled:
        has_public_targets = bool(public_pipeline.direct_candidates_by_service or public_pipeline.caddy_candidates)
        if has_public_targets:
            validation_issues.append(
                ValidationIssue(
                    severity="warning",
                    message=(
                        "no Zones rows with hairpin_allowed and valid Network values were found; "
                        "hairpin srcnat rules were skipped"
                    ),
                )
            )

    if not parsed_services:
        for service_key, reason in failures:
            print(f"Failed service '{service_key}': {reason}", file=sys.stderr)
        print(
            f"Processed {len(selected_services)} service(s): "
            f"{len(successes)} succeeded, {len(failures)} failed"
        )
        return 1

    for parsed in parsed_services:
        suppress_public_direct_rules = bool(
            parsed.exposure == "public"
            and parsed.ingress == "direct"
            and not parsed.use_input_chain
        )

        commands = _render_service_commands(
            parsed,
            include_public_direct_rules=suppress_public_direct_rules,
        )

        owned_public_candidates = public_pipeline.direct_candidates_by_service.get(parsed.service_key, [])
        if owned_public_candidates:
            commands.extend(
                _render_public_rule_bundle(
                    owned_public_candidates,
                    enable_hairpin=False,
                    place_before_filter_comment=place_before_filter_comment,
                )
            )

        if not commands:
            failures.append(
                (
                    parsed.service_key or parsed.service_name,
                    "no commands generated (check enabled sections and required fields)",
                )
            )
            continue

        timestamp = _dt.datetime.now(_dt.UTC).strftime("%Y-%m-%dT%H:%M:%SZ")
        lines = [
            f"# {COMMENT_MARKER}",
            f"# generated_at={timestamp}",
            f"# service={parsed.service_name}",
            f"# frontend_hostname={parsed.frontend_hostname or '(none)'}",
            "# address-list convention:",
            f"#   caddy hosts: {ADDRESS_LIST_CADDY_HOSTS}",
            f"#   backend groups: {ADDRESS_LIST_BACKEND_PREFIX}-<protocol>-<backend_port>",
            "# onboarding guidance:",
            "#   add/maintain caddy nodes:",
            (
                f"#     /ip firewall address-list add list={ADDRESS_LIST_CADDY_HOSTS} "
                'address=<CADDY_IP> comment="manual caddy node"'
            ),
            "#   add/maintain backend services:",
            (
                f"#     /ip firewall address-list add "
                f"list={ADDRESS_LIST_BACKEND_PREFIX}-<protocol>-<backend_port> "
                'address=<SERVICE_IP> comment="manual backend service"'
            ),
            "",
            *commands,
            "",
        ]
        content = "\n".join(lines)

        output_filename = f"{_normalized_service_filename(parsed.service_name)}.rsc"
        if output_filename.casefold() in generated_filenames:
            failures.append(
                (
                    parsed.service_key or parsed.service_name,
                    f"output filename collision for '{output_filename}' in current batch",
                )
            )
            continue
        generated_filenames.add(output_filename.casefold())

        output_path = output_dir / output_filename
        output_path.parent.mkdir(parents=True, exist_ok=True)
        output_path.write_text(content, encoding="utf-8")
        print(f"Wrote {output_path}")
        successes.append(output_path)

        if args.stdout:
            print(content)

    if successes and caddy_ip_resolved and public_pipeline.caddy_candidates:
        caddy_dstnat_content = _render_caddy_dstnat(
            caddy_ip_resolved,
            public_candidates=public_pipeline.caddy_candidates,
            enable_hairpin=False,
            place_before_filter_comment=place_before_filter_comment,
        )
        caddy_dstnat_path = output_dir / "caddy-dstnat.rsc"
        caddy_dstnat_path.parent.mkdir(parents=True, exist_ok=True)
        caddy_dstnat_path.write_text(caddy_dstnat_content, encoding="utf-8")
        print(f"Wrote {caddy_dstnat_path}")

    if successes and hairpin_source_entries:
        hairpin_rules_path = output_dir / "hairpin-rules.rsc"
        hairpin_rules_path.parent.mkdir(parents=True, exist_ok=True)
        hairpin_rules_path.write_text(
            _render_hairpin_rules(
                source_entries=hairpin_source_entries,
                candidates=hairpin_candidates,
            ),
            encoding="utf-8",
        )
        print(f"Wrote {hairpin_rules_path}")

    validation_issues = _dedupe_validation_issues(validation_issues)
    if validation_issues:
        validation_report_path = output_dir / "validation-report.txt"
        validation_report_path.parent.mkdir(parents=True, exist_ok=True)
        validation_report_path.write_text(
            _render_validation_report(validation_issues),
            encoding="utf-8",
        )
        print(f"Wrote {validation_report_path}")

    for service_key, reason in failures:
        print(f"Failed service '{service_key}': {reason}", file=sys.stderr)

    for issue in validation_issues:
        if issue.severity.strip().lower() == "error":
            scope = issue.service_key or "global"
            print(f"Validation error [{scope}]: {issue.message}", file=sys.stderr)

    print(
        f"Processed {len(selected_services)} service(s): "
        f"{len(successes)} succeeded, {len(failures)} failed"
    )

    if failures:
        return 1
    if any(issue.severity.strip().lower() == "error" for issue in validation_issues):
        return 1
    if not successes:
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
