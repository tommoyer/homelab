def _normalize_vlan_id(value: str | None) -> str | None:
    raw = (value or "").strip().lower()
    if not raw:
        return None
    match = re.search(r"(\d+)", raw)
    if not match:
        return None
    return str(int(match.group(1)))


def _dhcp_server_from_vlan_id(value: str | None) -> str | None:
    vlan_id = _normalize_vlan_id(value)
    if vlan_id is None:
        return None
    return f"dhcp-v{vlan_id}"


def _to_bool(value: str) -> bool:
    return value.strip().lower() in {"1", "y", "yes", "true", "on"}


def _dedupe_preserve_order(values: list[_T]) -> list[_T]:
    deduped: list[_T] = []
    seen: set[_T] = set()
    for value in values:
        if value in seen:
            continue
        seen.add(value)
        deduped.append(value)
    return deduped


def _parse_string_list(value: Any) -> list[str]:
    if isinstance(value, list):
        raw_values = [str(item).strip() for item in value]
    elif isinstance(value, str):
        raw_values = [part.strip() for part in re.split(r"[,;\n]+", value)]
    else:
        raw_values = []
    return [item for item in _dedupe_preserve_order(raw_values) if item]


def _parse_wan_ip_list(config: dict[str, Any]) -> tuple[list[str], list[str]]:
    fw_cfg = get_table(config, "mikrotik") or get_table(config, "firewall")
    valid: list[str] = []
    invalid: list[str] = []
    for raw in _parse_string_list(get_config_value(fw_cfg, "wan_ips", [])):
        normalized = normalize_ip(raw)
        if normalized:
            valid.append(normalized)
        else:
            invalid.append(raw)
    return _dedupe_preserve_order(valid), _dedupe_preserve_order(invalid)


def _normalize_network_cidr(value: Any) -> str | None:
    raw = as_str(value)
    if not raw:
        return None
    try:
        network = ipaddress.ip_network(raw, strict=False)
    except ValueError:
        return None
    return str(network)


def _load_management_address_list_from_zones_sheet(
    *,
    sheet_url: str,
    zones_gid: int,
    timeout_seconds: float,
) -> tuple[list[StaticAddressListEntry], list[ValidationIssue]]:
    if zones_gid < 0:
        return [], []

    df = _load_sheet_df(
        sheet_url=sheet_url,
        gid=int(zones_gid),
        timeout_seconds=timeout_seconds,
        label="Zones",
    )

    entries: list[StaticAddressListEntry] = []
    issues: list[ValidationIssue] = []
    seen: set[str] = set()

    for row_number, (_, row) in enumerate(df.iterrows(), start=2):
        if not _to_bool(as_str(row.get("allow_management")) or ""):
            continue

        subnet = _normalize_network_cidr(row.get("network"))
        if not subnet:
            issues.append(
                ValidationIssue(
                    severity="warning",
                    message=(
                        f"Zones row {row_number} is allow_management but is missing a valid Network value"
                    ),
                )
            )
            continue

        if subnet in seen:
            continue
        seen.add(subnet)
        entries.append(
            StaticAddressListEntry(
                list_name=ADDRESS_LIST_VLAN_MGMT,
                address=subnet,
                comment=ADDRESS_LIST_VLAN_MGMT,
            )
        )

    return entries, _dedupe_validation_issues(issues)


def _load_static_address_lists_from_sheet(
    *,
    sheet_url: str,
    static_address_lists_gid: int,
    timeout_seconds: float,
) -> tuple[list[StaticAddressListEntry], list[ValidationIssue]]:
    if static_address_lists_gid <= 0:
        return [], []

    df = _load_sheet_df(
        sheet_url=sheet_url,
        gid=int(static_address_lists_gid),
        timeout_seconds=timeout_seconds,
        label="Static Address Lists",
    )

    entries: list[StaticAddressListEntry] = []
    issues: list[ValidationIssue] = []
    seen: set[tuple[str, str]] = set()

    for row_number, (_, row) in enumerate(df.iterrows(), start=2):
        list_name = as_str(row.get("list"))
        address_raw = as_str(row.get("address"))

        if not list_name and not address_raw:
            continue
        if not list_name or not address_raw:
            issues.append(
                ValidationIssue(
                    severity="warning",
                    message=(
                        f"Static Address Lists row {row_number} must include both List and Address"
                    ),
                )
            )
            continue

        address = address_raw.strip() if (normalize_ip(address_raw) or _normalize_network_cidr(address_raw)) else None
        if not address:
            issues.append(
                ValidationIssue(
                    severity="warning",
                    message=(
                        f"Static Address Lists row {row_number} has invalid Address value: {address_raw}"
                    ),
                )
            )
            continue

        key = (list_name, address)
        if key in seen:
            continue
        seen.add(key)
        entries.append(
            StaticAddressListEntry(
                list_name=list_name,
                address=address,
                comment=f"static-address-list; {list_name}",
            )
        )

    return entries, _dedupe_validation_issues(issues)


