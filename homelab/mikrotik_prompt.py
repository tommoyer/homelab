def _render_manual_filter_rule_command(
    entry: ManualFilterRuleEntry,
    *,
    place_before_filter_comment: str | None = None,
    place_before_input_comment: str | None = None,
) -> str:
    parts = [
        f"/ip firewall filter add chain={entry.chain}",
        f"action={entry.action}",
    ]
    if entry.in_interface_list:
        parts.append(f"in-interface-list={entry.in_interface_list}")
    if entry.out_interface_list:
        parts.append(f"out-interface-list={entry.out_interface_list}")
    if entry.src_address_list:
        parts.append(f"src-address-list={entry.src_address_list}")
    if entry.dst_address_list:
        parts.append(f"dst-address-list={entry.dst_address_list}")
    if entry.protocol:
        parts.append(f"protocol={entry.protocol}")
    if entry.dst_port:
        parts.append(f"dst-port={entry.dst_port}")
    if entry.connection_state:
        parts.append(f"connection-state={entry.connection_state}")
    if entry.connection_nat_state:
        parts.append(f"connection-nat-state={entry.connection_nat_state}")
    if not entry.enabled:
        parts.append("disabled=yes")
    if entry.comment:
        parts.append(f"comment={mikrotik_quote(entry.comment)}")
    place_before = (
        place_before_input_comment if entry.chain == "input" else place_before_filter_comment
    )
    if place_before:
        parts.append(f"place-before=[find comment={mikrotik_quote(place_before)}]")
    return " ".join(parts)


