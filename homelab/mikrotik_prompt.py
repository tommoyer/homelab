def _load_manual_filter_rules_from_sheet(
    *,
    sheet_url: str,
    filter_rules_gid: int,
    timeout_seconds: float,
) -> tuple[list[ManualFilterRuleEntry], list[ValidationIssue]]:
    if filter_rules_gid <= 0:
        return [], []

    df = _load_sheet_df(
        sheet_url=sheet_url,
        gid=int(filter_rules_gid),
        timeout_seconds=timeout_seconds,
        label="Filter Rules",
    )

    entries: list[ManualFilterRuleEntry] = []
    issues: list[ValidationIssue] = []

    for row_number, (_, row) in enumerate(df.iterrows(), start=2):
        chain = as_str(row.get("chain")).strip().lower()
        order_raw = as_str(row.get("order")).strip()
        kind = as_str(row.get("kind")).strip().lower()
        action = as_str(row.get("action")).strip().lower()
        enabled_raw = as_str(row.get("enabled")).strip()

        if not chain and not order_raw and not kind:
            continue
        if kind not in {"rule", "anchor"}:
            issues.append(
                ValidationIssue(
                    severity="warning",
                    message=f"Filter Rules row {row_number}: unknown kind {kind!r}, skipping",
                )
            )
            continue
        if not chain:
            issues.append(
                ValidationIssue(
                    severity="warning",
                    message=f"Filter Rules row {row_number}: missing Chain, skipping",
                )
            )
            continue

        try:
            order = int(order_raw) if order_raw else 0
        except ValueError:
            issues.append(
                ValidationIssue(
                    severity="warning",
                    message=f"Filter Rules row {row_number}: invalid Order {order_raw!r}, defaulting to 0",
                )
            )
            order = 0

        enabled = enabled_raw.upper() not in {"FALSE", "0", "NO", "N"}

        comment_raw = as_str(row.get("comment")).strip() or None

        entries.append(
            ManualFilterRuleEntry(
                chain=chain,
                order=order,
                kind=kind,
                action=action,
                in_interface_list=as_str(row.get("in_interface_list")).strip() or None,
                out_interface_list=as_str(row.get("out_interface_list")).strip() or None,
                src_address_list=as_str(row.get("src_address_list")).strip() or None,
                dst_address_list=as_str(row.get("dst_address_list")).strip() or None,
                protocol=as_str(row.get("protocol")).strip().lower() or None,
                dst_port=as_str(row.get("dst_port")).strip() or None,
                connection_state=as_str(row.get("connection_state")).strip().lower() or None,
                connection_nat_state=as_str(row.get("connection_nat_state")).strip().lower() or None,
                enabled=enabled,
                comment=comment_raw,
                anchor_id=comment_raw if kind == "anchor" else None,
            )
        )

    return entries, _dedupe_validation_issues(issues)


