def _normalized_service_filename(service_name: str) -> str:
    raw = service_name.strip().lower()
    if not raw:
        return "service"
    normalized = re.sub(r"[^a-z0-9._-]+", "-", raw)
    normalized = normalized.strip("-_.")
    safe = sanitize_filename_component(normalized)
    return safe or "service"


