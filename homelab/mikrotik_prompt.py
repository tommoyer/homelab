def _normalize_vlan_id(value: str | None) -> str | None:
    raw = (value or "").strip().lower()
    if not raw:
        return None
    match = re.search(r"(\d+)", raw)
    if not match:
        return None
    return str(int(match.group(1)))


def _dhcp_server_from_vlan_id(value: str | None) -> str | None:
    vlan_id = _normalize_vlan_id(value)
    if vlan_id is None:
        return None
    return f"dhcp-v{vlan_id}"


def _to_bool(value: str) -> bool:
    return value.strip().lower() in {"1", "y", "yes", "true", "on"}


def _dedupe_preserve_order(values: list[_T]) -> list[_T]:
    deduped: list[_T] = []
    seen: set[_T] = set()
    for value in values:
        if value in seen:
            continue
        seen.add(value)
        deduped.append(value)
    return deduped


def _parse_string_list(value: Any) -> list[str]:
    if isinstance(value, list):
        raw_values = [str(item).strip() for item in value]
    elif isinstance(value, str):
        raw_values = [part.strip() for part in re.split(r"[,;\n]+", value)]
    else:
        raw_values = []
    return [item for item in _dedupe_preserve_order(raw_values) if item]


def _parse_wan_ip_list(config: dict[str, Any]) -> tuple[list[str], list[str]]:
    fw_cfg = get_table(config, "mikrotik") or get_table(config, "firewall")
    valid: list[str] = []
    invalid: list[str] = []
    for raw in _parse_string_list(get_config_value(fw_cfg, "wan_ips", [])):
        normalized = normalize_ip(raw)
        if normalized:
            valid.append(normalized)
        else:
            invalid.append(raw)
    return _dedupe_preserve_order(valid), _dedupe_preserve_order(invalid)


def _normalize_network_cidr(value: Any) -> str | None:
    raw = as_str(value)
    if not raw:
        return None
    try:
        network = ipaddress.ip_network(raw, strict=False)
    except ValueError:
        return None
    return str(network)


def _hairpin_zone_comment(row: Any) -> str:
    explicit = as_str(row.get("address_list"))
    if explicit:
        return explicit

    vlan_name = as_str(row.get("vlan_name"))
    if vlan_name:
        normalized = vlan_name.strip().lower().replace(" ", "-")
        if normalized.endswith("-lan"):
            return normalized
        return f"{normalized}-lan"

    zone_name = as_str(row.get("zone_name")) or as_str(row.get("dns_zone"))
    if zone_name:
        return zone_name.strip().lower().replace(" ", "-")

    return "hairpin-source"


def _load_hairpin_sources_from_zones_sheet(
    *,
    sheet_url: str,
    zones_gid: int,
    timeout_seconds: float,
) -> tuple[list[HairpinSourceEntry], list[ValidationIssue]]:
    if zones_gid < 0:
        return [], []

    df = _load_sheet_df(
        sheet_url=sheet_url,
        gid=int(zones_gid),
        timeout_seconds=timeout_seconds,
        label="Zones",
    )

    entries: list[HairpinSourceEntry] = []
    issues: list[ValidationIssue] = []
    seen: set[tuple[str, str]] = set()

    for row_number, (_, row) in enumerate(df.iterrows(), start=2):
        if not _to_bool(as_str(row.get("hairpin_allowed")) or ""):
            continue

        subnet = _normalize_network_cidr(row.get("network"))
        if not subnet:
            issues.append(
                ValidationIssue(
                    severity="warning",
                    message=(
                        f"Zones row {row_number} is hairpin_allowed but is missing a valid Network value"
                    ),
                )
            )
            continue

        comment = _hairpin_zone_comment(row)
        key = (subnet, comment)
        if key in seen:
            continue
        seen.add(key)
        entries.append(HairpinSourceEntry(subnet=subnet, comment=comment))

    return entries, _dedupe_validation_issues(issues)


def _parse_vlan_map(config: dict[str, Any]) -> dict[str, dict[str, str]]:
    fw_cfg = get_table(config, "mikrotik") or get_table(config, "firewall")

