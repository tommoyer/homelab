def _prefill_candidates_from_services_sheet(
    *,
    sheet_url: str,
    services_gid: int,
    timeout_seconds: float,
    service_key: str,
) -> list[dict[str, str | Any]]:
    service_key = service_key.strip().lower()
    if not service_key:
        return []

    df = _load_sheet_df(
        sheet_url=sheet_url,
        gid=int(services_gid),
        timeout_seconds=timeout_seconds,
        label="Services",
    )

    matches: list[dict[str, str | Any]] = []
    for _, row in df.iterrows():
        candidates = [
            as_str(row.get("service_name")),
            as_str(row.get("frontend_hostname")),
            as_str(row.get("hostname")),
        ]
        if service_key not in {c.lower() for c in candidates if c}:
            continue

        matches.append(
            {
                "service_name": as_str(row.get("service_name")),
                "frontend_hostname": as_str(row.get("frontend_hostname")),
                "hostname": as_str(row.get("hostname")),
                "protocol": as_str(row.get("protocol")).lower() or "tcp",
                "ingress": as_str(row.get("ingress")).lower() or "dstnat",
                "exposure": as_str(row.get("exposure")).lower() or "public",
                "frontend_ports": _ports_to_form_value(row.get("frontend_port")),
                "backend_ports": _ports_to_form_value(row.get("backend_port")),
                "backend_ip": as_str(row.get("ip_address")),
                "source_ip": as_str(row.get("source_ip")),
                "source_address_list": as_str(row.get("source_address_list")),
                "source_vlan": as_str(row.get("source_vlan")) or as_str(row.get("source_vlans")),
                "destination_vlan": as_str(row.get("destination_vlan")),
                "destination_address_list": as_str(row.get("destination_address_list")),
            }
        )

    return matches


def _prompt_select_service_by_name(
    *,
    service_key: str,
    matches: list[dict[str, str | Any]],
) -> dict[str, str | Any]:
    if not matches:
        return {}
    if len(matches) == 1:
        return matches[0]

    print(
        f"Multiple Services rows found for frontend hostname '{service_key}'.",
        file=sys.stderr,
    )
    print("Select the desired Service Name:", file=sys.stderr)

    for idx, match in enumerate(matches, start=1):
        service_name = str(match.get("service_name") or "").strip() or "(empty Service Name)"
        print(f"  [{idx}] {service_name}", file=sys.stderr)

    while True:
        print(f"Enter choice [1-{len(matches)}]: ", end="", file=sys.stderr, flush=True)
        choice = input().strip()
        if choice.isdigit():
            selected = int(choice)
            if 1 <= selected <= len(matches):
                return matches[selected - 1]
        print("Invalid choice. Try again.", file=sys.stderr)


