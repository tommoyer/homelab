def _run_single_input_prompt(initial_service_key: str) -> str:
    prompt = "Service key (service_name/frontend_hostname/hostname): "

    def _inner(stdscr: Any) -> str:
        curses.curs_set(1)
        stdscr.clear()
        stdscr.addstr(0, 0, "MikroTik single-service generator")
        stdscr.addstr(2, 0, prompt)
        stdscr.addstr(3, 0, "Type service key and press Enter")
        stdscr.refresh()

        buf = list(initial_service_key)
        stdscr.addstr(2, len(prompt), "".join(buf))
        pos = len(buf)
        while True:
            ch = stdscr.getch()
            if ch in {10, 13}:
                return "".join(buf).strip()
            if ch in {curses.KEY_BACKSPACE, 127, 8}:
                if pos > 0:
                    pos -= 1
                    del buf[pos]
            elif 32 <= ch <= 126:
                buf.insert(pos, chr(ch))
                pos += 1
            stdscr.move(2, len(prompt))
            stdscr.clrtoeol()
            stdscr.addstr(2, len(prompt), "".join(buf))
            stdscr.refresh()

    return curses.wrapper(_inner)


def _run_option_selection(
    stdscr: Any,
    *,
    title: str,
    options: list[str],
    current_value: str,
) -> str:
    if not options:
        return current_value

    choice_options = list(options)
    if current_value not in choice_options:
        choice_options.append(current_value)

    selected_idx = 0
    for i, item in enumerate(choice_options):
        if item == current_value:
            selected_idx = i
            break

    scroll = 0
    while True:
        stdscr.clear()
        height, width = stdscr.getmaxyx()
        stdscr.addstr(0, 0, f"Select value: {title}")
        stdscr.addstr(1, 0, "Use ↑/↓ to navigate, Enter to choose")

        visible_rows = max(1, height - 4)
        if selected_idx < scroll:
            scroll = selected_idx
        if selected_idx >= scroll + visible_rows:
            scroll = selected_idx - visible_rows + 1

        for row_idx in range(visible_rows):
            option_idx = scroll + row_idx
            if option_idx >= len(choice_options):
                break
            option = choice_options[option_idx]
            label = option if option else "(none)"
            prefix = ">" if option_idx == selected_idx else " "
            stdscr.addstr(3 + row_idx, 0, f"{prefix} {label}"[: max(1, width - 1)])

        ch = stdscr.getch()
        if ch == curses.KEY_UP:
            selected_idx = (selected_idx - 1) % len(choice_options)
            continue
        if ch == curses.KEY_DOWN:
            selected_idx = (selected_idx + 1) % len(choice_options)
            continue
        if ch in {10, 13}:
            return choice_options[selected_idx]


