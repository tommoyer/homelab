def _load_dhcp_leases_from_nodes_sheet(
    *,
    sheet_url: str,
    nodes_gid: int,
    timeout_seconds: float,
    lease_type_column: str,
    ip_column: str,
    mac_column: str,
    vlan_id_column: str,
) -> tuple[list[DhcpLeaseEntry], list[ValidationIssue]]:
    if nodes_gid < 0:
        return [], []

    df = _load_sheet_df(
        sheet_url=sheet_url,
        gid=int(nodes_gid),
        timeout_seconds=timeout_seconds,
        label="Nodes",
    )

    entries: list[DhcpLeaseEntry] = []
    issues: list[ValidationIssue] = []
    seen: set[tuple[str, str, str]] = set()

    for row_number, (_, row) in enumerate(df.iterrows(), start=2):
        lease_type = as_str(row.get(lease_type_column)).strip().lower()
        if lease_type != "dhcp":
            continue

        mac_address = as_str(row.get(mac_column)).strip()
        address = normalize_ip(as_str(row.get(ip_column)))
        server = _dhcp_server_from_vlan_id(as_str(row.get(vlan_id_column)))
        comment_source = as_str(row.get("hostname")) or as_str(row.get("dns_name")) or f"nodes-row-{row_number}"

        if not mac_address:
            issues.append(
                ValidationIssue(
                    severity="warning",
                    message=f"Nodes row {row_number} is DHCP but is missing a MAC Address value",
                )
            )
            continue

        if not address:
            issues.append(
                ValidationIssue(
                    severity="warning",
                    message=f"Nodes row {row_number} is DHCP but is missing a valid IP Address value",
                )
            )
            continue

        if not server:
            issues.append(
                ValidationIssue(
                    severity="warning",
                    message=(
                        f"Nodes row {row_number} is DHCP but is missing a valid VLAN ID value "
                        "(expected server format dhcp-v<vlan-id>)"
                    ),
                )
            )
            continue

        key = (mac_address.casefold(), address, server)
        if key in seen:
            continue
        seen.add(key)
        entries.append(
            DhcpLeaseEntry(
                mac_address=mac_address,
                address=address,
                server=server,
                comment=comment_source,
            )
        )

    return entries, _dedupe_validation_issues(issues)


