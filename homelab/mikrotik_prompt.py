def _to_bool(value: str) -> bool:
    return value.strip().lower() in {"1", "y", "yes", "true", "on"}


def _parse_vlan_map(config: dict[str, Any]) -> dict[str, dict[str, str]]:
    fw_cfg = get_table(config, "mikrotik") or get_table(config, "firewall")

    raw = fw_cfg.get("vlans", {})
    if not isinstance(raw, dict):
        return {}
    return {k.lower(): dict(v) for k, v in raw.items() if isinstance(v, dict)}


def _normalize_ports_lenient(value: Any) -> list[int]:
    if value is None:
        return []

    if isinstance(value, list):
        ports: list[int] = []
        for item in value:
            ports.extend(_normalize_ports_lenient(item))
        return ports

    if isinstance(value, (int, float)):
        try:
            parsed = int(float(value))
        except (TypeError, ValueError):
            return []
        return [parsed] if 1 <= parsed <= 65535 else []

    if isinstance(value, str):
        cleaned = value.strip()
        if not cleaned:
            return []
        parts = re.split(r"[\s,;/]+", cleaned)
        ports: list[int] = []
        for part in parts:
            token = part.strip()
            if not token:
                continue
            try:
                parsed = int(float(token))
            except ValueError:
                continue
            if 1 <= parsed <= 65535:
                ports.append(parsed)
        return ports

    return []


def _ports_to_form_value(value: Any) -> str:
    ports = _normalize_ports_lenient(value)
    return ",".join(str(port) for port in ports)


def _load_helpers_values(
    *,
    sheet_url: str,
    helpers_gid: int,
    timeout_seconds: float,
) -> dict[str, list[str]]:
    df = _load_sheet_df(
        sheet_url=sheet_url,
        gid=int(helpers_gid),
        timeout_seconds=timeout_seconds,
        label="Helpers",
    )

    values_by_column: dict[str, list[str]] = {}
    for column in df.columns:
        seen: set[str] = set()
        values: list[str] = []
        for raw in df[column]:
            value = as_str(raw)
            if not value:
                continue
            key = value.casefold()
            if key in seen:
                continue
            seen.add(key)
            values.append(value)
        values_by_column[column] = values

    return values_by_column


def _build_selection_options(
    *,
    helpers_values: dict[str, list[str]],
    vlan_map: dict[str, dict[str, str]],
) -> dict[str, list[str]]:
    helper_protocols = [value.lower() for value in helpers_values.get("protocols", []) if value.strip()]
    if not helper_protocols:
        raise ValueError("Helpers tab is missing required values for the 'Protocols' column.")

    helper_address_lists = [value for value in helpers_values.get("address_lists", []) if value.strip()]
    vlan_options = sorted(vlan_map.keys(), key=str.casefold)

    return {
        "protocol": helper_protocols,
        "ingress": ["dstnat", "direct", "caddy"],
        "exposure": ["public", "non-public"],
        "source_vlan": ["", *vlan_options],
        "destination_vlan": ["", *vlan_options],
        "source_address_list": ["", *helper_address_lists],
        "destination_address_list": ["", *helper_address_lists],
        "enable_nat": ["yes", "no"],
        "enable_filter": ["yes", "no"],
        "enable_dhcp": ["yes", "no"],
    }


def _normalized_service_filename(service_name: str) -> str:
    raw = service_name.strip().lower()
    if not raw:
        return "service"
    normalized = re.sub(r"[^a-z0-9._-]+", "-", raw)
    normalized = normalized.strip("-_.")
    safe = sanitize_filename_component(normalized)
    return safe or "service"


