def _prefill_from_nodes_sheet(
    *,
    sheet_url: str,
    nodes_gid: int,
    timeout_seconds: float,
    hostname: str,
    lease_type_column: str,
    ip_column: str,
    subnet_column: str,
    mac_column: str,
) -> dict[str, str]:
    target = hostname.strip().lower()
    if not target:
        return {}

    df = _load_sheet_df(
        sheet_url=sheet_url,
        gid=int(nodes_gid),
        timeout_seconds=timeout_seconds,
        label="Nodes",
    )
    for _, row in df.iterrows():
        node_name = as_str(row.get("hostname")).lower()
        dns_name = as_str(row.get("dns_name")).lower()
